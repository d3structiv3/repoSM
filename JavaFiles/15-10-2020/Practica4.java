    public void valTam(String name, int style, int s, String c) {
        if (s > 0) {
            if (c.equals("+")) {
                fuente = new Font(name, style, s + 10);
            } else {
                fuente = new Font(name, style, s - 10);
            }
        }
        lbl_titulo.setFont(fuente);
    }

